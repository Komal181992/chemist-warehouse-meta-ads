def clean_platforms(x):

    # Handle None / NaN
    if x is None:
        return None

    # If already a list
    if isinstance(x, list):
        return ', '.join(x)

    # If string representation of list
    if isinstance(x, str):
        try:
            parsed = ast.literal_eval(x)

            if isinstance(parsed, list):
                return ', '.join(parsed)

            return x

        except:
            return x

    return str(x)

