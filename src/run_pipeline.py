def run_dq_checks(final_df):

    checks = {
        'duplicate_ad_archive_id': final_df['ad_archive_id'].duplicated(keep=False).sum(),
        'missing_page_name': final_df['page_name'].isna().sum(),
        'invalid_start_date': final_df['start_date_formatted_dt'].isna().sum(),
        'future_start_date': (final_df['start_date_formatted_dt'] > pd.Timestamp.today()).sum(),
        'blank_creative_text': (
            final_df['snapshot.title'].isna()
            & final_df['snapshot.body.text'].isna()
        ).sum()
    }

    dq_summary = pd.DataFrame({
        'dq_check': checks.keys(),
        'failed_records': checks.values(),
        'total_records': len(final_df)
    })

    dq_summary['failure_rate_pct'] = (
        dq_summary['failed_records'] / dq_summary['total_records'] * 100
    ).round(2)

    dq_summary['pipeline_run_datetime'] = pd.Timestamp.now()

    return dq_summary

